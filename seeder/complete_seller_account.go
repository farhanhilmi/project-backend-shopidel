func generateRandomNumbers(min int, max int, count int) string {
	rand.Seed(time.Now().UnixNano()) // Seed the random number generator.
	numbers := make([]string, count)

	for i := 0; i < count; i++ {
		numbers[i] = fmt.Sprint(rand.Intn(max-min+1) + min)
	}

	return strings.Join(numbers, "")
}

