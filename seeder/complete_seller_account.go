func createProducts(db *gorm.DB, accountId int) {
	limit := len(products)
	if limit > 100 {
		limit = 100
	}

	for i := 0; i < limit; i++ {
		product := products[i]

		p := model.Products{
			Name:              product.Title,
			Description:       product.Detail,
			CategoryID:        product.CategoryId,
			SellerID:          accountId,
			HazardousMaterial: false,
			Weight:            decimal.NewFromInt(10),
			Size:              decimal.NewFromInt(20),
			IsNew:             true,
			InternalSKU:       "",
			ViewCount:         0,
			IsActive:          true,
		}

		if err := db.Create(&p).Error; err != nil {
			panic(err)
		}

		pv := model.ProductVariants{
			ProductID: p.ID,
			Name:      "default_reserved_keyword",
		}

		if err := db.Create(&pv).Error; err != nil {
			panic(err)
		}

		pvs := model.ProductVariantSelections{
			ProductVariantID: pv.ID,
			Name:             "default_reserved_keyword",
		}

		if err := db.Create(&pvs).Error; err != nil {
			panic(err)
		}

		pvsc := model.ProductVariantSelectionCombinations{
			ProductID:                  p.ID,
			ProductVariantSelectionID1: pvs.ID,
			Price:                      product.Price,
			Stock:                      rand.Intn(1000-10+1) + 10,
		}

		if err := db.Create(&pvsc).Error; err != nil {
			panic(err)
		}
	}

	products = append([]product{}, products[limit:]...)
}

func randomGender() string {
	gender := ""

	if rand.Int()%2 == 0 {
		gender = "male"
	} else {
		gender = "female"
	}

	return gender
}

